function Flyer()

	if c_etyItemModulesComponent ~= nil then
	
		if c_etyItemManagement ~= nil then
		
			c_etyItemModulesComponent:set_EquipmentToModule( c_etyItemManagement:get_Item( "LightEngine" 	) );
			c_etyItemModulesComponent:set_EquipmentToModule( c_etyItemManagement:get_Item( "LightGenerator" ) );
			c_etyItemModulesComponent:set_EquipmentToModule( c_etyItemManagement:get_Item( "LightShield" 	) );
			c_etyItemModulesComponent:set_EquipmentToModule( c_etyItemManagement:get_Item( "LightArmor" 	) );
			c_etyItemModulesComponent:set_EquipmentToModule( c_etyItemManagement:get_Item( "LightAmmoDump" 	) );
			
		end
		
	end
	
end